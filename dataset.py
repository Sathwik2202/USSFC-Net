import os
from PIL import Image
from torch.utils import data
import torchvision.transforms as transforms

class RsDataset(data.Dataset):
    def __init__(self, t1_p, t2_p, l_p, trans=None):
        self.files = sorted(os.listdir(t1_p))
        self.paths = [
            (os.path.join(t1_p, f),
             os.path.join(t2_p, f),
             os.path.join(l_p, f))
            for f in self.files
        ]
        self.trans = trans

    def __getitem__(self, i):
        t1, t2, l = [Image.open(p) for p in self.paths[i]]

        if self.trans:
            t1, t2 = self.trans(t1), self.trans(t2)

        l = transforms.ToTensor()(l)
        return t1, t2, l

    def __len__(self):
        return len(self.files)